bins <- cut(prices, breaks=3, labels=FALSE)
print("a) Bins: \n")
print(bins)
bin_means <- tapply(prices, bins, mean)
bin_boundary <- c(-Inf, unique(sort(prices)[(length(prices)/3)*(1:2)]), Inf)
print("b) Smoothed prices: \n")
smoothed_prices <- cut(prices, breaks=bin_boundary, labels=bin_means)
print(smoothed_prices)
